## 查看HTTP协议通信过程

# 1. 谷歌浏览器开发者工具使用 F12

#     开发者工具标签选项说明
#      元素(Elements) 用于查看或修改HTML标签
#      控制台(Console) 执行js代码
#      源代码(Sources) 查看静态资源文件,断电调试js代码
#      网络(Network) 查看HTTP协议通信过程

#     开发者工具使用说明
#      点击NetWork标签选项
#      在浏览器的地址栏输入百度网址,可以看到请求百度首页的HTTP通信过程
#      这里每项记录都是请求+响应一次过程

# 2. 小结
#     谷歌浏览器的开发者工具是查看HTTP协议的通信过程,通过NetWork标签选项可以查看每一次的请求和响应通信
#     开发者工具Headers选项总共有三部分组成
#      1. General:主要信息
#      2. Response Headers:响应头
#      3. Request Headers: 请求头
#     Response选项是查看响应体信息