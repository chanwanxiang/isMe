# 用户注册接口的设计和定义
#     设计接口基本思路
#     1)分析要实现的业务逻辑
#         明确在这个业务中设计到几个相关子业务
#         将每个子业务当做一个接口来设计
#     2)分析接口的功能任务,明确接口的访问方式和返回数据
#         请求方法(GET、POST、PUT、DELETE)
#         请求地址
#         请求参数(路径参数、查询字符串、表单、JSON)
#         响应数据(HTML、JSON等)

